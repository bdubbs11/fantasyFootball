<h4>Rosters:  There is no draft.  Each owner must pick their own roster subject to the limitations below.  Please be mindful of the roster restrictions for each team, this is necessary to prevent league owners from having similar rosters.</h4>
    <h4>(12 Players per Team): Quarterbacks (2), Running Backs (1), Wide Receivers (1), Tight End (1), Flex (RB, WR, or TE) (4), Kicker (1), Defense/Special Teams (1), Super Bowl winning Team (1)</h4>
    

    <p>Teams:    </p>
    <p>1. All teams must have at least <b>2 players</b> from a <b>6 or 7 seed.</b></p>
    <p>2. All teams must have at least 1 player from a <b>4 or 5 seed.</b></p>
    <p>3. All teams must have players from at least <b>9</b> different teams.</p>
    <p>4. Teams cannot have more than <b>3 players from 1 team (including Super Bowl winner)</b></p>
    <p>5. <b>NO STACKING</b> - Teams cannot have a Quarterback/WR/TE combo from the same team.  QB must be from a different team than your WR and/or TE, including the flex position.
    </p>
    <p>6. <b>The two quarterbacks</b> must be from different conferences and at least one of them <b>must</b> play on wildcard weekend.</p>
    <p>7. Teams are permitted no more than <b>4 players</b> combined from the <b>#1 seeds</b>
        (Baltimore and San Francisco), <b>including Super Bowl winner.</b></p>
