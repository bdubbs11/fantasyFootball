    <h1>Bonus Points</h1>

    <p><b>Doubles (2):</b> Any player who advances to the next round will be worth double the points for that game.</p>
    <p><b>Tripler (3):</b>  If players advance from the Wildcard round to the Conference Championship.  Their points will be triple value for that game. Or if a player advances from Divisionals to the Super Bowl.</p>
    <p><b>Quadruple (4):</b> If players from the Wildcard round advance to the Super Bowl they will receive quadruple points.</p>
    <p><b>SUPER BOWL BONUS:</b> If your team wins the Super Bowl, you will receive 14 points for every game they win. It will either be <b>42 or 56 points</b> depending on your team selection.</p>

    <h1>Payout</h1>

    <p>1st Place: 70%</p>
    <p>2nd Place: 30%</p>
    <p>3rd place: Money back</p>